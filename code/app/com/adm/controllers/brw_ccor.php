 	public function ejecutar($data)
 	{
 		$result = null;
 		try
 		{
 			$data['obtenerRowCount'] = true;
 			$data['cco_kycom'] = $_SESSION['com']['com_kycom'];
 			$data = $this->run("adm/GetListaCtacorriente",$data);

 			$result = $data['lista'];
